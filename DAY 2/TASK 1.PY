# Overview of Python

# Python is a high-level, interpreted, and easy-to-learn programming language.
# It was created by Guido van Rossum and is widely used because of its simple syntax and readability.

# Python allows developers to write programs using fewer lines of code, making it beginner-friendly and powerful at the same time. It supports multiple programming styles such as procedural, object-oriented, and functional programming.

# Python is platform-independent, meaning the same code can run on Windows, Linux, and macOS without changes.

